package chap2;

public class code02_07 {
    public static void main(String[] args) {

        int num1 = 200;
        int num2 = 100;

        int result = num1 + num2; //덧셈
        System.out.printf("%d + %d = %d\n", num1, num2, result);

        result = num1 - num2; //뺄셈
        System.out.printf("%d - %d = %d\n", num1, num2, result);

        result = num1 * num2; //곱셈
        System.out.printf("%d * %d = %d\n", num1, num2, result);

        result = num1 / num2; //나눗셈
        System.out.printf("%d / %d = %d\n", num1, num2, result);
    }
}
